export function computeOverallProgress(lessons: Lesson[], tasks: Task[]): number {
  const lessonDone = lessons.filter((lesson) => lesson.status === "completed").length;
  const taskDone = tasks.filter((task) => task.status === "completed").length;
  const total = lessons.length + tasks.length;
  if (total === 0) return 0;
  return Math.round(((lessonDone + taskDone) / total) * 100);
}

